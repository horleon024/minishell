#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	val;
	int	multiply;

	i = 0;
	val = 0;
	multiply = 1;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		multiply = -1;
	if (str[i] == '+' || multiply == -1)
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		val = val * 10 + (str[i] - '0');
		i++;
	}
	return (val * multiply);
}
