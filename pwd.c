#include "minishell.h"

int	ft_pwd(void)
{
	char	cwd[PATH_MAX];

	if (getcwd(cwd, PATH_MAX))
	{
		ft_putendl_fd(cwd, 1);
		g_global.return_code = 0;
		return (0);
	}
	g_global.return_code = 1;
	return (1);
}
